private void btnaceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaceptarActionPerformed
    int longitudprecio = txtpreciobus.getText().length();
    if(txtdireccion.getText().isEmpty()){
        showMessageError(NO_ARCHIVO);
    }else if(longitudprecio >= 5 || txtnombrebus.getText().isEmpty() || txtpreciobus.getText().isEmpty()|| CbxTipos.getSelectedIndex() == 0){
        showMessageError(CAMPOS_INCOMPLETOS);
    }else{
        try{
            int precio = Integer.parseInt(txtpreciobus.getText());
            int tipo = CbxTipos.getSelectedIndex();
            String type = CbxTipos.getSelectedItem().toString();
            String name = txtnombrebus.getText();
            try{
                if(type.equals("Metro")){
                    if(txtdireccion1.getText().trim().isEmpty()){
                        return;
                    }
                    lector = new LectorArchivo(txtdireccion.getText(),new TransporteModel(precio, name, tipo),txtdireccion1.getText());
                }else{
                    lector = new LectorArchivo(txtdireccion.getText(),new TransporteModel(precio, name, tipo),"");
                }
                txtdireccion.setText("");
                txtdireccion1.setText("");
                txtnombrebus.setText("");
                txtpreciobus.setText("");
                CbxTipos.setSelectedIndex(0); 
                showMessageInfo(ACEPTADO);
            }catch(Exception e){
               showMessageError(ERROR_GENERAL);
            }
        }catch (Exception ex){
            showMessageError(ERROR_PRECIO);
        }
    }
}//GEN-LAST:event_btnaceptarActionPerformed

