    private void llenarTipos()
    {
        LinkedList<String> tipos = ctrl.tipos();
        if(tipos == null){
            return;
        }
        CbxTipos.addItem("Seleccione un Tipo");
        for(int i = 0; i < tipos.size(); i++)
        {
            CbxTipos.addItem(tipos.get(i)); 
        }
    } 
